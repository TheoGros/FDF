#include "libft.h"

void	ft_putbignbr_u(unsigned long long int n)
{
	if (n >= 10)
	{
		ft_putbignbr_u(n / 10);
		ft_putchar((char)((n % 10) + '0'));
	}
	else
		ft_putchar((char)(n + '0'));
}
