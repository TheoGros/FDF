#include "libft.h"

void	ft_putnbr_u(unsigned int n)
{
	if (n >= 10)
	{
		ft_putnbr_u(n / 10);
		ft_putchar((char)((n % 10) + '0'));
	}
	else
		ft_putchar((char)(n + '0'));
}
